class SolutionSlow(object):
    def insert(self, itvls, newItvl):
        
        return self.merge(itvls+[newItvl])

    def merge(self, itvls):
        
        itvls.sort(cmp=lambda a, b: a.start - b.start)

        ret = [itvls[0]]
        for cur in itvls[1:]:
            pre = ret[-1]
            if cur.start <= pre.end:  
                pre.end = max(pre.end, cur.end)
            else:
                ret.append(cur)

        return ret


